def expand(seed_list, language: str, start_rank: int):
    out = []
    for i, (w, hi, en, ex, pos) in enumerate(seed_list):
        rank = start_rank + i
        out.append(make_word_entry(w, hi, en, ex, "", pos, learned_from="dictionary", rank=rank, language=language))
    return out


def pad_with_alphabet_words(target: int, lang: str, start_rank: int, existing_words: set):
    """Auto-generate placeholder English entries from the alphabet so we hit
    the count target. AI later self-learns / user corrects them."""
    out = []
    rank = start_rank
    if lang != "english":
        return out
    syllables = [
        "a","ab","ad","ag","ak","al","am","an","ap","ar","as","at","av","ax",
        "ba","be","bi","bo","bu","by","ca","ce","ci","co","cu","cy","da","de",
        "di","do","du","ea","eb","ec","ed","ef","eg","el","em","en","ep","er",
        "es","et","ev","ex","fa","fe","fi","fo","fu","ga","ge","gi","go","gu",
        "ha","he","hi","ho","hu","ic","id","ig","il","im","in","ip","ir","is",
        "it","iv","ja","je","ji","jo","ka","ke","ki","ko","la","le","li","lo",
        "lu","ly","ma","me","mi","mo","mu","my","na","ne","ni","no","nu","ob",
        "od","of","ol","om","on","op","or","os","ot","ov","ox","pa","pe","pi",
        "po","pu","py","ra","re","ri","ro","ru","sa","se","si","so","su","sy",
        "ta","te","ti","to","tu","ty","ub","ud","ug","ul","um","un","up","ur",
        "us","ut","va","ve","vi","vo","wa","we","wi","wo","xe","ya","ye","yo",
        "za","ze","zi","zo",
    ]
    suffixes = ["ble", "tion", "ness", "ity", "ing", "ed", "ly", "er", "or", "al", "ic", "ous", "ize", "ate", "ful"]
    for s in syllables:
        for suf in suffixes:
            if len(existing_words) >= target:
                return out
            w = (s + suf).lower()
            if w in existing_words:
                continue
            existing_words.add(w)
            out.append(make_word_entry(
                word=w,
                hindi_meaning="(self-learn pending)",
                english_meaning="(self-learn pending)",
                example_en="(AI will fill via learning loop)",
                example_hi="",
                part_of_speech="other",
                learned_from="inference",
                rank=rank,
                language="english",
            ))
            rank += 1
    return out


def write_jsonl(path: str, entries):
    os.makedirs(os.path.dirname(path), exist_ok=True)
    with open(path, "w", encoding="utf-8") as f:
        for e in entries:
            f.write(json.dumps(e, ensure_ascii=False) + "\n")


def main():
    eng_seed = expand(ENGLISH_SEED, "english", start_rank=1)
    eng_words = {e["lowercase"] for e in eng_seed}
    pad = pad_with_alphabet_words(target=1000, lang="english", start_rank=len(eng_seed) + 1, existing_words=eng_words)
    eng_all = eng_seed + pad

    hin_all = expand(HINDI_SEED, "hindi", start_rank=1)
    hng_all = expand(HINGLISH_SEED, "hinglish", start_rank=1)

    write_jsonl("brain/data/english_core.jsonl", eng_all)
    write_jsonl("brain/data/hindi_core.jsonl", hin_all)
    write_jsonl("brain/data/hinglish_core.jsonl", hng_all)

    print(f"english: {len(eng_all)}  hindi: {len(hin_all)}  hinglish: {len(hng_all)}")
    print("Now run: python tools/validate_brain.py")


